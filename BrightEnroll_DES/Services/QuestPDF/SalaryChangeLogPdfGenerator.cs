                                    header.Cell().Element(c => HeaderCellStyle(c)).Text("Net Change").FontSize(9).Bold();
                                    header.Cell().Element(c => HeaderCellStyle(c)).Text("Avg %").FontSize(9).Bold();
                                });

                                var yearIndex = 0;
                                foreach (var year in stats.SchoolYearBreakdown)
                                {
